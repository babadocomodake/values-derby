/**
 * お題バンクの検証。設計ガイド§7のルールを強制する。
 * 返り値は「問題点の文字列リスト」。空配列なら健全。
 * （ID重複なし / 選択肢ちょうど7・空文字なし・重複なし / genre・tone の妥当性）
 */
export function validateTopics(topics: Topic[]): string[] {
  const errors: string[] = [];
  const seen = new Set<string>();
  const GENRES = ["food", "love", "money", "life", "dilemma", "daily", "other"];
  const TONES = ["light", "mid"];

  for (const t of topics) {
    const where = `お題 ${t.id || "(IDなし)"}`;
    if (!t.id) errors.push(`${where}: id が空`);
    if (seen.has(t.id)) errors.push(`${where}: id が重複`);
    seen.add(t.id);
    if (!t.q || !t.q.trim()) errors.push(`${where}: 問い(q) が空`);
    if (t.opts.length !== 7) errors.push(`${where}: 選択肢が${t.opts.length}個（7個ちょうど必要）`);
    if (t.opts.some((o) => !o || !o.trim())) errors.push(`${where}: 空の選択肢がある`);
    if (new Set(t.opts).size !== t.opts.length) errors.push(`${where}: 選択肢に重複がある`);
    if (t.genre && !GENRES.includes(t.genre)) errors.push(`${where}: genre が不正 (${t.genre})`);
    if (t.tone && !TONES.includes(t.tone)) errors.push(`${where}: tone が不正 (${t.tone})`);
  }
  return errors;
}
